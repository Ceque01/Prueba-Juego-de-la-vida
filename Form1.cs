        private void PintarMatriz()
        {
            Bitmap bmp = new Bitmap(pictureBox1.Width, pictureBox1.Height);
            for (int x = 0; x < longitud; x++)
            {
                for (int y = 0; y < longitud; y++)
                {
                    if (celulas[x, y] == 0)
                        PintarPixel(bmp, x, y, Color.Black);
                    else
                        PintarPixel(bmp, x, y, Color.Green);
                }
            }

            pictureBox1.Image = bmp;
        }
        private void PintarPixel(Bitmap bmp, int x, int y, Color color)
        {
            for (int i = 0; i < longitudPixel; i++)
                for (int j = 0; j < longitudPixel; j++)
                    bmp.SetPixel(i + (x * longitudPixel), j + (y * longitudPixel), color);

        }

        private void JuegoDeLaVida()
        {
            int[,] celulasTemp = new int[longitud, longitud];
            for (int x = 0; x < longitud; x++)
            {
                for (int y = 0; y < longitud; y++)
                {
                    if (celulas[x, y] == 0)
                        celulasTemp[x, y] = ReglaJuegoDeLaVida(x, y, false);
                    else
                        celulasTemp[x, y] = ReglaJuegoDeLaVida(x, y, true);
                }
            }

            celulas = celulasTemp;

        }

        private int ReglaJuegoDeLaVida(int x, int y, bool EsViva)
        {
            int VecinasVivas = 0;

            //Celula V1
            if (x > 0 && y > 0)
                if (celulas[x - 1, y - 1] == 1)
                    VecinasVivas++;

            //Celula V2
            if (y > 0)
                if (celulas[x, y - 1] == 1)
                    VecinasVivas++;

            //Celula V3
            if (x < longitud - 1 && y > 0)
                if (celulas[x + 1, y - 1] == 1)
                    VecinasVivas++;

            //Celula V4
            if (x > 0)
                if (celulas[x - 1, y] == 1)
                    VecinasVivas++;

            //Celula V5
            if (x < longitud - 1)
                if (celulas[x + 1, y] == 1)
                    VecinasVivas++;

            //Celula 6
            if (x > 0 && y < longitud - 1)
                if (celulas[x - 1, y + 1] == 1)
                    VecinasVivas++;

            //Celula V7
            if (y < longitud - 1)
                if (celulas[x, y + 1] == 1)
                    VecinasVivas++;


            //Celula V8
            if (x < longitud - 1 && y < longitud - 1)
                if (celulas[x + 1, y + 1] == 1)
                    VecinasVivas++;

            if (EsViva)
                return (VecinasVivas == 2 || VecinasVivas == 3) ? 1 : 0;
            else
                return VecinasVivas == 3 ? 1 : 0;

        }

        private void button3_Click(object sender, EventArgs e)
        {
            //reinicio
            for (int i = 0; i < longitud; i++)
                for (int j = 0; j < longitud; j++)
                    celulas[i, j] = 0;

            Random r = new Random();
            //llenar random
            for (int i = 0; i < longitud; i++)
                for (int j = 0; j < longitud; j++)
                    celulas[i, j] = r.Next(0, 2);

            PintarMatriz();
        }

